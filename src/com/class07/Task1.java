package com.class07;

public class Task1 {

	public static void main(String[] args) {
		/*
		 * 
		 * Print even numbers from 2-100
		 * 
		 */
		
		int e=20;
		
		while (e<=100) {
			System.out.println(e+" ");
			e+=2;
		}
		
		System.out.println();
		System.out.println("----------------------------------------------");
		
		
		int a=20;
		
		while(a<=100) {
			
			if (a%2==0) {
			System.out.println();
			}
			a++;
		}
	}

}
